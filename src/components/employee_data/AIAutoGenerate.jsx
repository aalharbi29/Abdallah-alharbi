import React, { useState } from 'react';
import { Button } from '@/components/ui/button';
import { Sparkles, Loader2 } from 'lucide-react';
import { base44 } from '@/api/base44Client';
import { toast } from 'sonner';
import { formatAssignmentPeriodPlain } from '@/components/employee_data/periodUtils';

/**
 * يولّد عنوان التقرير والنص التعبيري تلقائياً بالذكاء الاصطناعي
 * بناءً على: أسماء المكلفين + تخصصاتهم + جهات التكليف + مجموعات الفترات + الأيام المحددة
 *
 * النمط المعتمد:
 * - العنوان: "خطة تغطية مركز [الجهة المكلَّف بها] - [التخصص]"
 * - النص: ترويسة ثابتة ("سعادة مدير مستشفى الحسو العام ... السلام عليكم ورحمة الله وبركاته")
 *   يليها نص رسمي يذكر تمتع [تخصص] المركز بإجازته، ويرفع خطة التغطية بتكليف [التخصص]/الاسم،
 *   مع الفترة/المدة/الأيام، ويُختم بـ "نأمل التكرم بالاطلاع والنظر بخطة التغطية ..."
 */
export default function AIAutoGenerate({
  selectedEmployees,
  assignmentCenters,
  assignmentGroups,
  onApply, // ({ title, narrative }) => void
}) {
  const [loading, setLoading] = useState(false);

  const handleGenerate = async () => {
    if (!selectedEmployees || selectedEmployees.length === 0) {
      toast.error('الرجاء اختيار الموظفين المكلفين أولاً');
      return;
    }

    setLoading(true);
    try {
      // بناء ملخص بيانات المكلفين مع فترة كل موظف
      const employeesSummary = selectedEmployees.map((emp) => {
        const assignCenter = assignmentCenters?.[emp.id];
        const assignedTo = assignCenter
          ? (assignCenter.includes('شؤون') ? assignCenter : `مركز ${assignCenter}`)
          : '';

        // استخراج نص فترة التكليف الخاصة بهذا الموظف
        let periodText = '';
        for (const group of assignmentGroups || []) {
          const ids = group.employeeIds?.length > 0 ? group.employeeIds : selectedEmployees.map(e => e.id);
          if (ids.includes(emp.id)) {
            const text = formatAssignmentPeriodPlain(group, emp.id);
            if (text && text !== '-') { periodText = text; break; }
          }
        }

        return {
          name: emp.full_name_arabic || '',
          position: emp.position || '',
          gender: emp.gender || '',
          workplace: emp.المركز_الصحي || '',
          assigned_to: assignedTo,
          assigned_to_raw: assignCenter || '',
          period_text: periodText || '',
        };
      });

      const isSingle = employeesSummary.length === 1;

      const prompt = `أنت مساعد متخصص في صياغة الخطابات الإدارية الرسمية باللغة العربية الفصحى بأسلوب وزاري حكومي دقيق. مهمتك توليد "عنوان التقرير" و"النص التعبيري" فقط، وفقاً للنمط المعتمد أدناه حرفياً.

البيانات المتاحة:

الموظفون المكلفون (مع تخصصاتهم وجهات تكليفهم وفترة تكليف كل واحد):
${JSON.stringify(employeesSummary, null, 2)}

عدد المكلفين: ${employeesSummary.length} (${isSingle ? 'حالة فردية' : 'حالة متعددة'})

==========================
النمط المعتمد للعنوان:
==========================
"خطة تغطية [الجهة المكلَّف بها من حقل assigned_to] - [التخصص]"

أمثلة:
- إذا كان التخصص "طبيب" والتكليف إلى "مركز الحسو" ⇒ "خطة تغطية مركز الحسو - طبيب"
- إذا كان التخصص "ممرض" أو "ممرضة" ⇒ "خطة تغطية مركز [كذا] - تمريض"
- إذا كان "صيدلي" ⇒ "خطة تغطية مركز [كذا] - صيدلة"
- عمّم القاعدة على أي تخصص آخر (مختبر، أشعة، أسنان، إلخ) بنفس الأسلوب.

إذا تعدد المكلفون بتخصصات مختلفة لنفس المركز ⇒ اجمع التخصصات بفاصلة. مثال: "خطة تغطية مركز الحسو - طبيب، تمريض".
إذا تعددت جهات التكليف ⇒ استخدم أقرب صياغة جامعة بنفس النسق.

==========================
النمط المعتمد للنص التعبيري:
==========================
ابدأ النص حرفياً بترويسة الخطاب كما يلي (مع الحفاظ على المسافات والأسطر كما هي تماماً):

سعادة مدير مستشفى الحسو العام                   حفظه الله
     السلام عليكم ورحمة الله وبركاته

ثم يليها سطر فارغ، ثم الفقرة الرسمية. انتبه: عدد المكلفين يحدد صياغة الفقرة كما يلي:

--- الحالة الفردية (موظف واحد فقط) ---
اكتب فقرة واحدة بهذا النسق:
"نظراً لتمتع [تخصص الموظف] مركز [assigned_to] بإجازته السنوية، نرفع لكم خطة تغطية المركز حيث نقترح تكليف [التخصص] / [اسم الموظف] [إن وُجدت period_text أضف: ' اعتباراً من [period_text]'] [إن لم توجد period_text اكتب: ' اعتباراً من .......']. نأمل التكرم بالاطلاع والنظر بخطة التغطية، وفي حال إقرارها نرجو الرفع لمن يلزم بإصدار قرار التكليف."

مثال تطبيقي للحالة الفردية:
"نظراً لتمتع طبيب مركز صحي الحسو بإجازته السنوية، نرفع لكم خطة تغطية المركز حيث نقترح تكليف الطبيب / محمد أحمد اعتباراً من 1446/10/01 هـ إلى 1446/10/15 هـ. نأمل التكرم بالاطلاع والنظر بخطة التغطية، وفي حال إقرارها نرجو الرفع لمن يلزم بإصدار قرار التكليف."

--- الحالة المتعددة (موظفان أو أكثر) ---
اكتب النص دائماً بهذا النسق الحرفي: مقدمة جامعة ثم قائمة مرقمة، كل موظف في سطر منفصل، ثم خاتمة.

المقدمة:
"بناءً على تمتع [تخصص الموظفين بصيغة الجمع المناسبة] المراكز التالية ([أسماء المراكز من assigned_to_raw فقط، مفصولة بـ " - "]) بإجازاتهم السنوية، نرفع لكم خطة تغطية مراكزهم حيث نقترح تكليف كل من:"

القائمة المرقمة — طبّق السطر الصحيح لكل موظف حسب حقل gender:
- إذا كان gender = "ذكر":
"1- يُكلَّف [التخصص بصيغة المذكر] / [الاسم] بتغطية مركز [assigned_to_raw] اعتباراً من [period_text]."
- إذا كان gender = "أنثى":
"1- تُكلَّف [التخصص بصيغة المؤنث] / [الاسم] بتغطية مركز [assigned_to_raw] اعتباراً من [period_text]."

استبدل الرقم لكل موظف (1-، 2-، 3-، ...)، واترك كل بند في سطر منفصل تماماً. لا تستخدم "يغطي"؛ استخدم دائماً "بتغطية".

الخاتمة حرفياً:
"نأمل التكرم بالاطلاع والنظر بخطة التغطية، وفي حال إقرارها نرجو الأمر على من يلزم بإصدار قرار التكليف."

مثال تطبيقي إلزامي عند تعدد الأطباء:
"بناءً على تمتع أطباء المراكز التالية (الحسو - طلال - هدبان) بإجازاتهم السنوية، نرفع لكم خطة تغطية مراكزهم حيث نقترح تكليف كل من:
1- يُكلَّف الطبيب / محمد يوسف الغمراوي بتغطية مركز الحسو اعتباراً من 1448/02/12 هـ إلى 1448/02/22 هـ (أيام: الثلاثاء، الأحد، الخميس).
2- يُكلَّف الطبيب / طارق أسعد قناوي بتغطية مركز طلال اعتباراً من 1448/02/20 هـ إلى 1448/03/20 هـ (أيام: الاثنين، الأربعاء).
3- يُكلَّف الطبيب / محمد عبدالله علي بتغطية مركز هدبان اعتباراً من 1448/02/20 هـ إلى 1448/03/20 هـ (أيام: الاثنين، الأربعاء).
نأمل التكرم بالاطلاع والنظر بخطة التغطية، وفي حال إقرارها نرجو الأمر على من يلزم بإصدار قرار التكليف."

قواعد حرجة:
- حافظ حرفياً على ترويسة "سعادة مدير مستشفى الحسو العام                   حفظه الله" والسطر "     السلام عليكم ورحمة الله وبركاته" دون أي تغيير في المسافات أو الألفاظ.
- استخدم التخصص المناسب حسب الجنس: طبيب/طبيبة، ممرض/ممرضة، صيدلي/صيدلانية، فني/فنية. لا تستخدم صيغة المذكر للأنثى أو العكس.
- استخدم صيغة الجمع المناسبة في المقدمة: أطباء للطبيب، طبيبات للطبيبة، ممرضون أو ممرضات وفق بيانات الموظفين، وصياغة جامعة مناسبة عند اختلاف التخصصات.
- في الحالة المتعددة: كل موظف في سطر منفصل برقم وشارة (1-، 2-، ...) ولا تدمجهم في فقرة واحدة.
- استخدم period_text كما هو إن وُجد (ويحتوي على التواريخ والأيام جاهزة). إن لم توجد period_text اكتب "اعتباراً من .......".
- لا تذكر أسماء موظفين آخرين غير المكلفين المذكورين في employeesSummary.
- لا تضف أي مقدمات أو تعليقات خارج حقل narrative.
- أعِد النتيجة كـ JSON فقط.`;

      const response = await base44.integrations.Core.InvokeLLM({
        prompt,
        response_json_schema: {
          type: 'object',
          properties: {
            title: { type: 'string', description: 'عنوان التقرير الرسمي حسب النمط' },
            narrative: { type: 'string', description: 'النص التعبيري الكامل شاملاً الترويسة' },
          },
          required: ['title', 'narrative'],
        },
      });

      if (response?.title || response?.narrative) {
        onApply({ title: response.title || '', narrative: response.narrative || '' });
        toast.success('تم توليد العنوان والنص التعبيري تلقائياً');
      } else {
        toast.error('لم يتم توليد نتيجة — حاول مرة أخرى');
      }
    } catch (e) {
      console.error(e);
      toast.error('حدث خطأ أثناء التوليد');
    } finally {
      setLoading(false);
    }
  };

  return (
    <Button
      type="button"
      size="sm"
      onClick={handleGenerate}
      disabled={loading}
      className="bg-gradient-to-r from-purple-600 to-blue-600 hover:from-purple-700 hover:to-blue-700 gap-1"
    >
      {loading ? (
        <>
          <Loader2 className="w-4 h-4 animate-spin" /> جاري التوليد...
        </>
      ) : (
        <>
          <Sparkles className="w-4 h-4" /> توليد العنوان والنص تلقائياً
        </>
      )}
    </Button>
  );
}