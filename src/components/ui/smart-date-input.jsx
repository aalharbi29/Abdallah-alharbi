const toEnglishDigits = (str) => {
  if (!str) return "";
  const map = {"٠":"0","١":"1","٢":"2","٣":"3","٤":"4","٥":"5","٦":"6","٧":"7","٨":"8","٩":"9"};
  return String(str).replace(/[٠-٩]/g, d => map[d] || d);
};

