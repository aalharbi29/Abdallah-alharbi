// حساب عدد الأيام من بداية التقويم الهجري (1/1/1) إلى تاريخ معين
function hijriToJulianDay(hYear, hMonth, hDay) {
  // نقطة البداية: 1 محرم 1 هجري = 16 يوليو 622 ميلادي = Julian Day 1948440
  const baseJD = 1948440;
  
  // حساب عدد الأيام من بداية التقويم
  let days = 0;
  
  // إضافة أيام السنوات الكاملة
  for (let y = 1; y < hYear; y++) {
    if (ummAlQuraData[y]) {
      days += ummAlQuraData[y].reduce((a, b) => a + b, 0);
    } else {
      // إذا لم تكن البيانات متوفرة، استخدم متوسط السنة الهجرية (354 يوم)
      days += 354;
    }
  }
  
  // إضافة أيام الأشهر الكاملة في السنة الحالية
  if (ummAlQuraData[hYear]) {
    for (let m = 0; m < hMonth - 1; m++) {
      days += ummAlQuraData[hYear][m];
    }
  } else {
    // استخدم المتوسط (29.5 يوم لكل شهر)
    days += Math.floor((hMonth - 1) * 29.5);
  }
  
  // إضافة الأيام في الشهر الحالي
  days += hDay - 1;
  
  return baseJD + days;
}

// تحويل Julian Day إلى تاريخ ميلادي
function julianDayToGregorian(jd) {
  const a = jd + 32044;
  const b = Math.floor((4 * a + 3) / 146097);
  const c = a - Math.floor((146097 * b) / 4);
  const d = Math.floor((4 * c + 3) / 1461);
  const e = c - Math.floor((1461 * d) / 4);
  const m = Math.floor((5 * e + 2) / 153);
  
  const day = e - Math.floor((153 * m + 2) / 5) + 1;
  const month = m + 3 - 12 * Math.floor(m / 10);
  const year = 100 * b + d - 4800 + Math.floor(m / 10);
  
  return { year, month, day };
}

// التحويل من الهجري إلى الميلادي باستخدام تقويم أم القرى
function convertHijriToGregorian(hijriString) {
  try {
    const parts = hijriString.split('/');
    if (parts.length !== 3) return '';
    
    const hDay = parseInt(parts[0]);
    const hMonth = parseInt(parts[1]);
    const hYear = parseInt(parts[2]);
    
    if (!hDay || !hMonth || !hYear || hDay < 1 || hDay > 30 || hMonth < 1 || hMonth > 12) {
      return '';
    }
    
    // تحويل باستخدام تقويم أم القرى
    const jd = hijriToJulianDay(hYear, hMonth, hDay);
    const greg = julianDayToGregorian(jd);
    
    // تنسيق التاريخ
    const year = greg.year.toString();
    const month = greg.month.toString().padStart(2, '0');
    const day = greg.day.toString().padStart(2, '0');
    
    return `${year}-${month}-${day}`;
  } catch (error) {
    console.error('Hijri conversion error:', error);
    return '';
  }
}

const parseHijriInput = (input) => {
  const cleaned = input.replace(/[^\d]/g, ' ').trim();
  const parts = cleaned.split(/\s+/);
  
  if (parts.length === 3) {
    const day = parts[0].padStart(2, '0');
    const month = parts[1].padStart(2, '0');
    const year = parts[2];
    return `${day}/${month}/${year}`;
  }
  return input;
};

export default function SmartDateInput({ 
  label, 
  value, 
  onChange, 
  placeholder = "أدخل التاريخ...",
  required = false 
}) {
  const [inputMode, setInputMode] = useState('gregorian');
  const [displayValue, setDisplayValue] = useState('');
  const [convertedValue, setConvertedValue] = useState('');

  useEffect(() => {
    if (value) {
      setDisplayValue(value);
      if (inputMode === 'gregorian') {
        const hijriEquivalent = convertGregorianToHijri(value);
        setConvertedValue(hijriEquivalent);
      }
    }
  }, [value, inputMode]);

  const handleInputChange = (e) => {
    const inputValue = e.target.value;
    setDisplayValue(inputValue);

    if (inputMode === 'gregorian') {
      if (inputValue.match(/^\d{4}-\d{2}-\d{2}$/)) {
        const hijriEquivalent = convertGregorianToHijri(inputValue);
        setConvertedValue(hijriEquivalent);
        onChange(inputValue);
      }
    } else {
      const parsedHijri = parseHijriInput(inputValue);
      
      if (parsedHijri.match(/^\d{2}\/\d{2}\/\d{4}$/)) {
        const gregorianEquivalent = convertHijriToGregorian(parsedHijri);
        if (gregorianEquivalent) {
          setConvertedValue(gregorianEquivalent);
          onChange(gregorianEquivalent);
        }
      }
    }
  };

  const toggleInputMode = () => {
    setInputMode(prev => prev === 'gregorian' ? 'hijri' : 'gregorian');
    setDisplayValue('');
    setConvertedValue('');
  };

  const getPlaceholder = () => {
    if (inputMode === 'gregorian') {
      return "2024-01-15 (ميلادي)";
    } else {
      return "15/5/1445 (هجري)";
    }
  };

  const getInputType = () => {
    return inputMode === 'gregorian' ? 'date' : 'text';
  };

  return (
    <div className="space-y-2">
      <div className="flex items-center justify-between">
        <Label>{label} {required && <span className="text-red-500">*</span>}</Label>
        <Button
          type="button"
          variant="ghost"
          size="sm"
          onClick={toggleInputMode}
          className="text-xs flex items-center gap-1"
        >
          <RefreshCw className="w-3 h-3" />
          {inputMode === 'gregorian' ? 'تبديل للهجري' : 'تبديل للميلادي'}
        </Button>
      </div>
      
      <div className="space-y-2">
        <Input
          type={getInputType()}
          value={displayValue}
          onChange={handleInputChange}
          placeholder={getPlaceholder()}
          required={required}
          className={`${inputMode === 'hijri' ? 'text-right' : ''}`}
        />
        
        {convertedValue && (
          <div className="text-sm text-gray-600 bg-blue-50 p-2 rounded border">
            <div className="flex items-center gap-2">
              <Calendar className="w-4 h-4 text-blue-500" />
              <span>
                {inputMode === 'gregorian' 
                  ? `التاريخ الهجري المقابل: ${convertedValue}`
                  : `التاريخ الميلادي المقابل: ${convertedValue}`
                }
              </span>
            </div>
          </div>
        )}
      </div>
      
      <div className="text-xs text-gray-500">
        {inputMode === 'hijri' && "يمكنك الإدخال بالصيغ: 15/5/1445 أو 15-5-1445 أو 15 5 1445"}
      </div>
    </div>
  );
}